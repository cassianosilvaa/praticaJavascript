let celsius = Number(
    prompt("Escreva aqui uma temperatura em °C para fazer a conversão em F")
);
let fahrenheit = (celsius * 9) / 5 + 32;

alert(`${celsius} graus Celsius é ${fahrenheit} graus Fahrenheit!`);

